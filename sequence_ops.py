# * ! # octothorpe
#   #! is a hash-bang

print(f"'a' * 10: {'a' * 10}")
print('-' * 60)
language = "Python"
print(f"language * 3: {language * 3}")

print(f"[False] * 10: {[False] * 10}")

stuff = ['a', 'b', 'c']
print(f"stuff * 5: {stuff * 5}")

a = ['we', 'are']
b = ['the', 'champions']
c = a + b
print(f"c: {c}")

